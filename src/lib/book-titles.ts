export const getBookTitle = (filename: string): string => {
    // Extract number from filename (e.g., "01-Main.pdf" -> "01")
    const match = filename.match(/^(\d+)/);
    if (match && match[1]) {
        return BOOK_TITLES[match[1]] || `Big History Book ${match[1]}`;
    }
    return filename;
};
